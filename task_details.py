    """Generate JavaScript for smooth scroll navigation."""
    return """
    <script>
        // Check localStorage availability
        function isLocalStorageAvailable() {
            try {
                const test = '__storage_test__';
                localStorage.setItem(test, test);
                localStorage.removeItem(test);
                return true;
            } catch (e) {
                return false;
            }
        }
        
        // Smooth scroll to task function
        function scrollToTask(taskId) {
            const element = document.getElementById(taskId);
            if (element) {
                // Offset for sticky header
                const headerOffset = 80;
                const elementPosition = element.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;
                
                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });
                
                // Highlight the section briefly
                element.classList.add('highlight');
                setTimeout(() => element.classList.remove('highlight'), 2000);
            }
            return false; // Prevent default anchor behavior
        }
        
        // Sidebar toggle functionality
        function toggleSidebar() {
            const sidebar = document.getElementById('sidebar');
            const toggle = document.getElementById('sidebar-toggle');
            const docArea = document.querySelector('.document-area');
            
            sidebar.classList.toggle('collapsed');
            toggle.classList.toggle('collapsed');
            if (docArea) {
                docArea.classList.toggle('full-width');
            }
            
            // Update button text
            toggle.textContent = sidebar.classList.contains('collapsed') 
                ? 'â–¶ ì²´í¬ë¦¬ìŠ¤íŠ¸' 
                : 'ì²´í¬ë¦¬ìŠ¤íŠ¸ â—€';
            
            // Save preference
            if (isLocalStorageAvailable()) {
                localStorage.setItem('sidebar-collapsed', sidebar.classList.contains('collapsed'));
            }
        }
        
        // ============================================
        // Hierarchical Checkbox System
        // ============================================
        const SUBTASK_STORAGE_KEY = 'pivot-protocol-subtasks';
        const SIDEBAR_STORAGE_KEY = 'pivot-protocol-sidebar-progress';
        
        // Initialize hierarchical checkboxes
        function initHierarchicalCheckboxes() {
            // When a sub-task checkbox changes
            document.querySelectorAll('.sub-task-cb').forEach(cb => {
                cb.addEventListener('change', function() {
                    const parentId = this.dataset.parent;
                    checkParentCompletion(parentId);
                    saveSubTaskProgress();
                });
            });
            
            // Initialize sidebar task progress indicators
            initTaskProgressIndicators();
        }
        
        // Initialize progress indicators based on requirement counts
        function initTaskProgressIndicators() {
            // Get unique parent IDs
            const parents = new Set();
            document.querySelectorAll('.sub-task-cb').forEach(cb => {
                parents.add(cb.dataset.parent);
            });
            
            // Update each task's progress indicator
            parents.forEach(parentId => {
                updateTaskProgress(parentId);
            });
        }
        
        // Check if all sub-tasks are complete and update parent
        function checkParentCompletion(parentId) {
            // Get all sub-tasks for this parent
            const subTasks = document.querySelectorAll(`.sub-task-cb[data-parent="${parentId}"]`);
            const allChecked = Array.from(subTasks).every(cb => cb.checked);
            
            // Find the parent checkbox in sidebar
            const parentCheckbox = document.querySelector(`.task-cb[data-task="${parentId}"]`);
            if (parentCheckbox) {
                parentCheckbox.checked = allChecked;
                const label = parentCheckbox.closest('.checklist-item');
                if (label) {
                    label.classList.toggle('completed', allChecked);
                }
            }
            
            // Update the progress indicator
            updateTaskProgress(parentId);
            
            // Update overall progress
            updateSidebarProgress();
        }
        
        // Update the progress indicator for a specific task
        function updateTaskProgress(parentId) {
            const subTasks = document.querySelectorAll(`.sub-task-cb[data-parent="${parentId}"]`);
            const checked = Array.from(subTasks).filter(cb => cb.checked).length;
            const total = subTasks.length;
            
            // Update progress in sidebar
            const sidebarProgress = document.querySelector(`.task-progress[data-task="${parentId}"]`);
            if (sidebarProgress) {
                sidebarProgress.textContent = `(${checked}/${total})`;
                sidebarProgress.classList.toggle('complete', checked === total);
            }
            
            // Update progress in task detail
            const detailProgress = document.querySelector(`.req-progress[data-task="${parentId}"]`);
            if (detailProgress) {
                detailProgress.textContent = `(${checked}/${total})`;
                detailProgress.classList.toggle('complete', checked === total);
            }
        }
        
        // Save sub-task progress to localStorage
        function saveSubTaskProgress() {
            if (!isLocalStorageAvailable()) return;
            const progress = {};
            document.querySelectorAll('.sub-task-cb').forEach(cb => {
                const key = `${cb.dataset.parent}-${cb.dataset.sub}`;
                progress[key] = cb.checked;
            });
            localStorage.setItem(SUBTASK_STORAGE_KEY, JSON.stringify(progress));
        }
        
        // Load sub-task progress from localStorage
        function loadSubTaskProgress() {
            if (!isLocalStorageAvailable()) return;
            const saved = localStorage.getItem(SUBTASK_STORAGE_KEY);
            if (saved) {
                try {
                    const progress = JSON.parse(saved);
                    document.querySelectorAll('.sub-task-cb').forEach(cb => {
                        const key = `${cb.dataset.parent}-${cb.dataset.sub}`;
                        if (progress[key]) {
                            cb.checked = true;
                        }
                    });
                    
                    // Update all parent checkboxes
                    const parents = new Set();
                    document.querySelectorAll('.sub-task-cb').forEach(cb => {
                        parents.add(cb.dataset.parent);
                    });
                    parents.forEach(parentId => checkParentCompletion(parentId));
                } catch (e) {
                    // Silent fail - JSON parse error for sub-task progress
                }
            }
        }
        
        // Update sidebar overall progress
        function updateSidebarProgress() {
            const total = document.querySelectorAll('.task-cb').length;
            const completed = document.querySelectorAll('.task-cb:checked').length;
            const percent = total > 0 ? Math.round((completed / total) * 100) : 0;
            
            const progressBar = document.getElementById('sidebar-progress');
            if (progressBar) {
                progressBar.style.width = percent + '%';
            }
            
            const progressText = document.getElementById('sidebar-progress-text');
            if (progressText) {
                progressText.textContent = completed + '/' + total;
            }
        }
        
        // Load sidebar collapsed state
        function loadSidebarState() {
            if (!isLocalStorageAvailable()) return;
            const collapsed = localStorage.getItem('sidebar-collapsed');
            if (collapsed === 'true') {
                const sidebar = document.getElementById('sidebar');
                const toggle = document.getElementById('sidebar-toggle');
                const docArea = document.querySelector('.document-area');
                if (sidebar && toggle) {
                    sidebar.classList.add('collapsed');
                    toggle.classList.add('collapsed');
                    toggle.textContent = 'â–¶ ì²´í¬ë¦¬ìŠ¤íŠ¸';
                    if (docArea) {
                        docArea.classList.add('full-width');
                    }
                }
            }
        }
        
        // Initialize on page load
        document.addEventListener('DOMContentLoaded', function() {
            initHierarchicalCheckboxes();
            loadSubTaskProgress();
            loadSidebarState();
        });
    </script>"""
